	    // },
	    /*
		*
		* */
        methods: {
            /*
            * Получение доп. информации депозита
            * */
            depositProperty() {
                this.$http.get(`/1/deposit/${this.$route.params.id}/property/`)
                    .then(({ data }) => {
                        if (data.results.length) {
                            this.additionalDetails = data.results
                        }

                        this.layer = true
                    })
            }

        }
    }
